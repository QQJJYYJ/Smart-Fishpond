//第一组
document.getElementById('part1').addEventListener('mouseenter', function() {
  document.getElementById('part1').style.backgroundColor = 'rgb(9, 65, 255)'; // 鼠标悬浮时的背景色
  document.getElementById('part1').style.color = '#fff'; // 鼠标悬浮时的文字
  document.getElementById('part1Dot').style.backgroundColor = 'rgb(9, 65, 255)'; 
});
 
document.getElementById('part1').addEventListener('mouseleave', function() {
  document.getElementById('part1').style.backgroundColor = '#fff'; // 鼠标离开时的背景色、
  document.getElementById('part1').style.color = '#000'; // 鼠标离开时的文字
  document.getElementById('part1Dot').style.backgroundColor = '#c0c0c0';
});



